public class switch_case {
    public static void main(String[] args) {
        int diaDeLaSemana = 2;

        switch(diaDeLaSemana){
            case 1:
                System.out.println("Lunes");
                break;
            case 2:
                System.out.println("Martes");
                break;
            default:
                System.out.println("Dia no valido");
        }
    }
}
